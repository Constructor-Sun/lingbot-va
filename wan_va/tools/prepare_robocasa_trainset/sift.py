def load_motion_flags(flags_csv: Path, tasks=None):
    tasks = set(tasks or [])
    grouped = {}
    seen_keys = set()

    with flags_csv.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        required = {"task_name", "collection_id", "dataset_id", "episode_index", "is_moving"}
        missing = required.difference(reader.fieldnames or [])
        if missing:
            raise ValueError(f"{flags_csv} missing columns: {sorted(missing)}")

        for row in reader:
            task_name = row["task_name"]
            if tasks and task_name not in tasks:
                continue

            collection_id = row["collection_id"]
            dataset_id = row["dataset_id"]
            source_episode_index = int(row["episode_index"])
            is_moving = parse_bool(row["is_moving"])
            key = (task_name, collection_id, dataset_id, source_episode_index)
            if key in seen_keys:
                raise ValueError(f"Duplicate CSV row for {key}")
            seen_keys.add(key)

            grouped.setdefault((task_name, collection_id, dataset_id), []).append(
                {
                    "source_episode_index": source_episode_index,
                    "is_moving": is_moving,
                }
            )

    for key, rows in grouped.items():
        rows.sort(key=lambda x: x["source_episode_index"])

    return grouped


def parse_bool(value: str) -> bool:
    normalized = value.strip().lower()
    if normalized in {"true", "1", "yes"}:
        return True
    if normalized in {"false", "0", "no"}:
        return False
    raise ValueError(f"Cannot parse bool from {value!r}")


def read_json(path: Path):
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def write_json(path: Path, data):
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=True, indent=4)
        f.write("\n")


def read_jsonl(path: Path):
    rows = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def write_jsonl(path: Path, rows):
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=True))
            f.write("\n")


def safe_remove_tree(path: Path):
    if path.exists():
        shutil.rmtree(path)


def maybe_copy_file(src: Path, dst: Path, dry_run: bool):
    if dry_run:
        return
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)


def maybe_write_json(path: Path, data, dry_run: bool):
    if dry_run:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    write_json(path, data)


def maybe_write_jsonl(path: Path, rows, dry_run: bool):
    if dry_run:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    write_jsonl(path, rows)


def maybe_write_csv(path: Path, rows, fieldnames, dry_run: bool):
    if dry_run:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


