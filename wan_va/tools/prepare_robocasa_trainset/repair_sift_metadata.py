#!/usr/bin/env python3
import argparse
import json
import os
from pathlib import Path

import numpy as np
import pandas as pd


def _find_counterfactual_root() -> Path:
    env = os.environ.get("COUNTERFACTUAL_ROOT")
    if env:
        return Path(env)
    start = Path(__file__).resolve().parent
    for ancestor in [start] + list(start.parents):
        if (ancestor / ".git").is_dir() and (ancestor / "robust_wam").is_dir():
            return ancestor
    raise RuntimeError("Cannot find counterfactual project root. Set COUNTERFACTUAL_ROOT.")


_CF_ROOT = _find_counterfactual_root()
_EXP_ROOT = _CF_ROOT.parent

_DATASET = os.environ.get("ROBOCASA_DATASET_PATH")
DEFAULT_DATASET_ROOT = Path(_DATASET) if _DATASET else (_EXP_ROOT / "robocasa" / "datasets" / "training_no_base" / "atomic")


def parse_args():
    parser = argparse.ArgumentParser(
        description=(
            "Backfill count-bearing RoboCasa LeRobot metadata generated by sift.py "
            "without touching videos, latents, or parquet payloads."
        )
    )
    parser.add_argument(
        "--dataset-root",
        type=Path,
        default=DEFAULT_DATASET_ROOT,
        help="Root like robocasa/datasets/training_no_base/atomic.",
    )
    parser.add_argument(
        "--tasks",
        type=str,
        nargs="*",
        default=None,
        help="Optional task names. If omitted, all task folders under dataset-root are processed.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print intended updates without writing files.",
    )
    return parser.parse_args()


def read_json(path: Path):
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def write_json(path: Path, data):
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=True, indent=4)
        f.write("\n")


def read_jsonl(path: Path):
    rows = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def write_jsonl(path: Path, rows):
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=True))
            f.write("\n")


def summarize_numeric_vector(values: np.ndarray):
    if values.size == 0:
        return {
            "mean": [0.0],
            "std": [0.0],
            "min": [0.0],
            "max": [0.0],
            "count": [0],
            "q01": [0.0],
            "q99": [0.0],
        }
    return {
        "mean": [float(np.mean(values))],
        "std": [float(np.std(values))],
        "min": [float(np.min(values))],
        "max": [float(np.max(values))],
        "count": [int(values.size)],
        "q01": [float(np.quantile(values, 0.01))],
        "q99": [float(np.quantile(values, 0.99))],
    }


def extract_vector_series(series: pd.Series):
    if len(series) == 0:
        return np.empty((0, 0), dtype=np.float64)
    first = series.iloc[0]
    if np.isscalar(first) or isinstance(first, (bool, np.bool_)):
        arr = series.to_numpy()
        return np.asarray(arr, dtype=np.float64).reshape(-1, 1)
    stacked = np.vstack(series.to_numpy())
    return np.asarray(stacked, dtype=np.float64)


def compute_feature_stats(df: pd.DataFrame):
    stats = {}
    for column in df.columns:
        if column.startswith("observation.images."):
            continue

        values = extract_vector_series(df[column])
        if values.shape[0] == 0:
            continue

        stats[column] = {
            "mean": np.mean(values, axis=0).tolist(),
            "std": np.std(values, axis=0).tolist(),
            "min": np.min(values, axis=0).tolist(),
            "max": np.max(values, axis=0).tolist(),
            "count": [int(values.shape[0])],
            "q01": np.quantile(values, 0.01, axis=0).tolist(),
            "q99": np.quantile(values, 0.99, axis=0).tolist(),
        }
    return stats


def aggregate_feature_stats(rows):
    means = np.stack([np.asarray(row["mean"], dtype=np.float64) for row in rows])
    variances = np.stack([np.asarray(row["std"], dtype=np.float64) ** 2 for row in rows])
    counts = np.asarray([float(row["count"][0]) for row in rows], dtype=np.float64)
    total_count = counts.sum()
    if total_count <= 0:
        raise ValueError("Cannot aggregate stats with non-positive total count")

    count_shape = (len(rows),) + (1,) * (means.ndim - 1)
    weights = counts.reshape(count_shape)
    total_mean = np.sum(means * weights, axis=0) / total_count
    delta = means - total_mean
    total_variance = np.sum((variances + delta**2) * weights, axis=0) / total_count

    aggregated = {
        "min": np.min(np.stack([np.asarray(row["min"], dtype=np.float64) for row in rows]), axis=0).tolist(),
        "max": np.max(np.stack([np.asarray(row["max"], dtype=np.float64) for row in rows]), axis=0).tolist(),
        "mean": total_mean.tolist(),
        "std": np.sqrt(np.maximum(total_variance, 0.0)).tolist(),
        "count": [int(total_count)],
    }

    quantile_keys = sorted(
        key for key in rows[0].keys() if key.startswith("q") and key[1:].isdigit()
    )
    for q_key in quantile_keys:
        if not all(q_key in row for row in rows):
            continue
        q_values = np.stack([np.asarray(row[q_key], dtype=np.float64) for row in rows])
        aggregated[q_key] = (np.sum(q_values * weights, axis=0) / total_count).tolist()

    return aggregated


def aggregate_fallback_stats(episode_stats_rows):
    aggregated = {}
    feature_keys = {key for row in episode_stats_rows for key in row["stats"]}
    for feature_key in sorted(feature_keys):
        rows = [row["stats"][feature_key] for row in episode_stats_rows if feature_key in row["stats"]]
        aggregated[feature_key] = aggregate_feature_stats(rows)
    return aggregated


def repair_episode_stats_rows(episodes_rows, episode_stats_rows):
    lengths_by_episode = {
        int(row["episode_index"]): int(row["length"]) for row in episodes_rows
    }
    repaired_rows = []
    global_index_start = 0

    for row in sorted(episode_stats_rows, key=lambda item: int(item["episode_index"])):
        updated = json.loads(json.dumps(row))
        episode_index = int(updated["episode_index"])
        frame_count = lengths_by_episode[episode_index]

        updated["stats"]["episode_index"] = summarize_numeric_vector(
            np.full(frame_count, episode_index, dtype=np.float64)
        )
        updated["stats"]["frame_index"] = summarize_numeric_vector(
            np.arange(frame_count, dtype=np.float64)
        )
        updated["stats"]["index"] = summarize_numeric_vector(
            np.arange(global_index_start, global_index_start + frame_count, dtype=np.float64)
        )
        global_index_start += frame_count
        repaired_rows.append(updated)

    missing = []
    for row in repaired_rows:
        for feature_key, feature_stats in row["stats"].items():
            if "count" not in feature_stats:
                missing.append((row["episode_index"], feature_key))
    if missing:
        preview = ", ".join(f"ep{ep}:{key}" for ep, key in missing[:10])
        raise ValueError(f"Unexpected stats without count after repair: {preview}")

    return repaired_rows


def rebuild_task_stats(task_dir: Path, repaired_episode_stats):
    parquet_paths = sorted((task_dir / "data").glob("*/*.parquet"))
    if not parquet_paths:
        raise FileNotFoundError(f"No parquet files found under {task_dir / 'data'}")

    dataframes = [pd.read_parquet(path) for path in parquet_paths]
    computed_stats = compute_feature_stats(pd.concat(dataframes, ignore_index=True))
    fallback_stats = aggregate_fallback_stats(repaired_episode_stats)

    for feature_key, feature_stats in fallback_stats.items():
        if feature_key.startswith("observation.images.") or feature_key not in computed_stats:
            computed_stats[feature_key] = feature_stats

    return computed_stats


def process_task(task_dir: Path, dry_run: bool):
    meta_dir = task_dir / "meta"
    episodes_path = meta_dir / "episodes.jsonl"
    episodes_stats_path = meta_dir / "episodes_stats.jsonl"
    stats_path = meta_dir / "stats.json"

    episodes = read_jsonl(episodes_path)
    episode_stats = read_jsonl(episodes_stats_path)

    repaired_episode_stats = repair_episode_stats_rows(episodes, episode_stats)
    rebuilt_stats = rebuild_task_stats(task_dir, repaired_episode_stats)

    if dry_run:
        print(f"[dry-run] {task_dir.name}: would rewrite episodes_stats.jsonl and stats.json")
        return

    write_jsonl(episodes_stats_path, repaired_episode_stats)
    write_json(stats_path, rebuilt_stats)
    print(f"[done] {task_dir.name}: repaired metadata only")


def main():
    args = parse_args()
    dataset_root = args.dataset_root
    if not dataset_root.exists():
        raise FileNotFoundError(f"Dataset root does not exist: {dataset_root}")

    if args.tasks:
        task_dirs = [dataset_root / task for task in args.tasks]
    else:
        task_dirs = sorted(
            path for path in dataset_root.iterdir() if path.is_dir() and (path / "meta").is_dir()
        )

    for task_dir in task_dirs:
        if not task_dir.exists():
            raise FileNotFoundError(f"Task directory does not exist: {task_dir}")
        process_task(task_dir, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
